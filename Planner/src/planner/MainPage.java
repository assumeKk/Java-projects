    public ArrayList<Semester> getSemesterInfo(String semesterName) throws ClassNotFoundException, SQLException {
        ArrayList<Semester> temp = new ArrayList<>();
        Semester semTemp = new Semester();
        Connection c = null;
        Statement stmt = null;
        Class.forName("org.sqlite.JDBC");
        c = DriverManager.getConnection("jdbc:sqlite:planner.db");
        c.setAutoCommit(false);
        System.out.println("Opened database successfully");

        stmt = c.createStatement();
        ResultSet rs = stmt.executeQuery("SELECT SEMESTER.SEMESTER_NAME, MODULE.MODULE_ID, MODULE.MODULE_NAME,"
                + "COURSEWORK.COURSEWORK_ID, COURSEWORK.COURSEWORK_NAME, COURSEWORK.COURSEWORK_DEADLINE, COURSEWORK.COURSEWOR_WEIGHT,"
                + "EXAM.EXAM_ID, EXAM.EXAM_DATE, EXAM.EXAM_LENGTH, EXAM.EXAM_WEIGHT"
                + "WHERE SEMESTER_NAME ='" + semesterName + "' "
                + "AND MODULE.SEMESTER_ID = SEMESTER.SEMESTER_ID "
                + "AND COURSEWORK.MODULE_ID = MODULE.MODULE_ID "
                + "AND EXAM.MODULE_ID = MODULE.MODULE_ID;");

        while (rs.next()) {
            Module moduleTemp = new Module();
            Coursework cwTemp = new Coursework();
            ExamInfo examTemp = new ExamInfo();
            String semName = rs.getString("SEMESTER_NAME");
            String modId = rs.getString("MODULE_ID");
            String modName = rs.getString("MODULE_NAME");
            int courId = rs.getInt("COURSEWORK_ID");
            String courName = rs.getString("COURSEWORK_NAME");
            String courDeadline = rs.getString("COURSEWORK_DEADLINE");
            int courWeight = rs.getInt("COURSEWOR_WEIGHT");
            int examId = rs.getInt("EXAM_ID");
            String examDate1 = rs.getString("EXAM_DATE");
            int examLength1 = rs.getInt("EXAM_LENGTH");
            int examWeight1 = rs.getInt("EXAM_WEIGHT");
            semTemp.setSemesterName(semName);
            moduleTemp.setModuleID(modId);
            moduleTemp.setModuleName(modName);
            cwTemp.setCourseworkName(courName);
            cwTemp.setDeadline(courDeadline);
            cwTemp.setMarkValue(courWeight);
            examTemp.setExamDate(examDate1);
            examTemp.setLength(examLength1);
            examTemp.setMarkValue(examWeight1);
            moduleTemp.addCoursework(cwTemp);
            moduleTemp.addExam(examTemp);
            semTemp.addModule(moduleTemp);
            temp.add(semTemp);
        }
        rs.close();
        stmt.close();
        c.commit();
        c.close();
        return temp;
    }

